    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        appear();
        btnEditar.setVisible(false);
        btnExcluir.setVisible(false);
        tabelaAgenda.setVisible(false);
        if (tabelaAgenda.getSelectedRow() != -1) {

            campoNomeAlterado.setText((String) tabelaAgenda.getValueAt(tabelaAgenda.getSelectedRow(), 1));
            campoEmailAlterado.setText((String) tabelaAgenda.getValueAt(tabelaAgenda.getSelectedRow(), 2));
            campoSenhaAlterado.setText((String) tabelaAgenda.getValueAt(tabelaAgenda.getSelectedRow(), 3));

        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        Usuario usuario = new Usuario();
        UsuarioDAO uDAO = new UsuarioDAO();
        usuario.setNome(campoNomeAlterado.getText());
        usuario.setEmail(campoEmailAlterado.getText());
        usuario.setSenha(campoSenhaAlterado.getText());
        usuario.setId((int) tabelaAgenda.getValueAt(tabelaAgenda.getSelectedRow(), 0));
        uDAO.update(usuario);
        blind();
        tabelaAgenda.setVisible(true);
        readTable();
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int id;
        UsuarioDAO uDAO = new UsuarioDAO();
        if (JOptionPane.showConfirmDialog(rootPane, "Deseja excluir?", "Excluir", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            id = (int) tabelaAgenda.getValueAt(tabelaAgenda.getSelectedRow(), 0);
            uDAO.delete(id);
            readTable();
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

