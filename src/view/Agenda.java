    public void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaAgenda.getModel();
        modelo.setNumRows(0);
        UsuarioDAO uDAO = new UsuarioDAO();

        uDAO.read().forEach((usuario) -> {
            modelo.addRow(new Object[]{
                usuario.getId(),
                usuario.getNome(),
                usuario.getEmail(),
                usuario.getSenha(),});
        });
    }

